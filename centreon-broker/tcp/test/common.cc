#include <cstdlib>
#include "test/common.hh"

/**
 *  Return a random port.
 *
 *  @return Random port to listen on or connect to.
 */
unsigned short random_port() {
  return (rand() % (MAX_PORT - MIN_PORT + 1) + MIN_PORT);
}
